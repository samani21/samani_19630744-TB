    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Koneksi koneksi = new Koneksi();
            Connection con = koneksi.getConnection();
            PreparedStatement ps;
            if(status==SEDANG_TAMBAH){
                String executeQuery =  "Insert into tb_tiket "
                + "(kelas,maskapai,penerbangan,tujuan,tanggal,jam,biaya) value(?,?,?,?,?,?,?)";
                ps = con.prepareStatement(executeQuery);
                ps.setString(1, cbKelas.getSelectedItem().toString());
                ps.setString(2, cbMaskapai.getSelectedItem().toString().trim());
                ps.setString(3, cbPenerbangan.getSelectedItem().toString().trim());
                ps.setString(4, cbTujuan.getSelectedItem().toString().trim());
                ps.setString(5, eTanggal.getText());
                ps.setString(6, cbJam.getSelectedItem().toString());
                ps.setString(7, eTotal.getText());
                JOptionPane.showMessageDialog(null, "Berhasil simpan jadwal");
                ListTiket  list = new ListTiket();
                list.setVisible(true);
            }else{
                String executeQuery =  "UPDATE tb_tiket SET "
                        + "kelas=?,maskapai=?,penerbangan=?,tujuan=?,tanggal=?,jam=?,biaya=? where id=?";
                ps = con.prepareStatement(executeQuery);
                ps.setString(1, cbKelas.getSelectedItem().toString());
                ps.setString(2, cbMaskapai.getSelectedItem().toString().trim());
                ps.setString(3, cbPenerbangan.getSelectedItem().toString().trim());
                ps.setString(4, cbTujuan.getSelectedItem().toString().trim());
                ps.setString(5, eTanggal.getText());
                ps.setString(6, cbJam.getSelectedItem().toString());
                ps.setString(7, eTotal.getText());
                ps.setString(8, eId.getText());
                JOptionPane.showMessageDialog(null, "Berhasil Edit jadwal");
                ListTiket  list = new ListTiket();
                list.setVisible(true);
            }
            ps.executeUpdate();
        }catch(SQLException ex){
            System.err.println(ex);
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

