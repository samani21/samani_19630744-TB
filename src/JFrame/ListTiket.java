    public ArrayList<Tiket> getTiketList(String keyword){
     ArrayList<Tiket> tiketList = new ArrayList<Tiket>();
     Koneksi koneksi =  new Koneksi();
     Connection connection = koneksi.getConnection();
     
     String query ="SELECT * FROM tb_tiket"+keyword;
     Statement st;
     ResultSet rs;
     
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         while (rs.next()){
             tiket = new Tiket(
                     rs.getInt("id"),
                     rs.getString("kelas"),
                     rs.getString("maskapai"),
                     rs.getString("penerbangan"),
                     rs.getString("tujuan"),
                     rs.getString("tanggal"),
                     rs.getString("jam"),
                     rs.getString("biaya"));
             tiketList.add(tiket);
        
         }
     } catch (SQLException | NullPointerException ex){
         System.err.println("Koneksi Null Gagal");
     }
        return tiketList;
    }
    
    public void selectTiket(String keyword){
        ArrayList<Tiket> list = getTiketList(keyword);
        DefaultTableModel model = (DefaultTableModel)tbTiket.getModel();
        Object[] row = new Object[8];
        
        for (int i = 0; i <list.size(); i++){
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getKelas();
            row[2] = list.get(i).getMaskapai();
            row[3] = list.get(i).getPenerbangan();
            row[4] = list.get(i).getTujuan();
            row[5] = list.get(i).getTanggal();
            row[6] = list.get(i).getJam();
            row[7] = list.get(i).getBiaya();
            
            model.addRow(row);
        }
    }
    
    public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)tbTiket.getModel();
        model.setRowCount(0);
        selectTiket(keyword);
    }
